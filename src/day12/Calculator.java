package day12;

public class Calculator {

    //두 정수를 더하는 메서드
    //매개 변수의 개수 -> 2개
    public int add(int a, int b){
        return a +  b;
    }

    //세 정수를 더하는 메서드
    //메개 변수의 개수 -> 3개
    public int add(int a, int b, int c){
        return a + b + c;
    }

    //두 실수를 더하는 메서드
    //매개 변수의 개수 -> 2개, 매개 변수의 변수의 타입이 double
    public double add(double a, double b){
        return a + b;
    }
}


